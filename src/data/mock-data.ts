function buildUseCase(
  partial: Omit<UseCase, "innovationScore" | "badges"> & { innovationScore?: number }
): UseCase {
  const score =
    partial.innovationScore ??
    calculateInnovationScore(
      partial.votes,
      partial.impact,
      partial.effort,
      partial.comments.length,
      Math.floor((Date.now() - new Date(partial.createdAt).getTime()) / 86400000)
    );
  const uc: UseCase = { ...partial, innovationScore: score, badges: [] };
  uc.badges = deriveUseCaseBadges(uc);
  return uc;
}

